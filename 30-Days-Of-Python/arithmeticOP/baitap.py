# Giá rau: 15 000 đ/kg

# Giá thịt: 120 000 đ/kg

# Bạn mua 2 kg rau và 0.5 kg thịt thì tổng số tiền phải trả là bao nhiêu?

so_tien_di_cho = 100000

rau_kg = 15000
thit_kg = 120000
so_rau = 2
so_thit = 0.5

tongTien = rau_kg * so_rau + thit_kg * so_thit

tongSoTienDiCho = so_tien_di_cho - tongTien

print("Tổng số tiền phải trả là:", tongTien)

print("Số tiền thừa lại là:", tongSoTienDiCho)